import numpy as np

def print_x():
    for y in np.arange(-5, 6, 0.5):
        x = 2 * y - 7
        print(x, end='|')


def print_b():
    for a in np.arange(-5, 6, 0.5):
        b = 2 - 2 * a ** 2
        print(b, end='|')

print("----------------------------------------------------------------------------------------------------------------")
print_x()
print()
print("---------------------------------------------------------------------------------------------------------------------")
print_b()
print()
print("---------------------------------------------------------------------------------------------------------------------")
