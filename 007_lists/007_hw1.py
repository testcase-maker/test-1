my_lst = [1, 9, -104, 22, 0, 333, 43, 88]

x = sum(my_lst)
y = len(my_lst)

print("Это список чисел - ", my_lst)
print("Максимальный элемент в списке - ", max(my_lst))
print("Минимальный элемент в списке - ", min(my_lst))
print("Сумма элементов списка - ", x)
print('Количество элементов списка - ', y)
print('Среднее арифметическое - ', x / y)
